export const translations: Record<AppLanguage, NestedStrings> = { en, sv }

/** Resolve a dot-notation key like "nav.dashboard" from the translation tree. */
function resolve(tree: NestedStrings, key: string): string | undefined {
  const parts = key.split('.')
  let cur: string | NestedStrings | undefined = tree
  for (const p of parts) {
    if (cur === undefined || typeof cur === 'string') return undefined
    cur = (cur as NestedStrings)[p]
  }
  return typeof cur === 'string' ? cur : undefined
}

export function translate(language: AppLanguage, key: string): string {
  return (
    resolve(translations[language], key) ??
    resolve(translations['en'], key) ??
    key
  )
}
